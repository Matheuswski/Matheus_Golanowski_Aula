# Faça um programa que o usuario digite um  numero e diga se o NUMERO É SUPERIOR A 20 INFERIOR A 10 ou se ESTÁ ENTRE 10 E 20
num1 = int(input("Digite qualquer numero: "))
if num1 > 20:
    print("Seu nome é maior que 20")
elif num1 < 10:
    print("Seu numero é menor que 10")
else:
    print("Seu numero esta entre 10 e 20")        